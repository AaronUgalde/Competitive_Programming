void solve(int testcase){
    int n; cin >> n;
    vector<int> A(n);
    for(auto &a : A) cin >> a;

    int ans = 0;
    int curr_strength = INT_MAX;
    for(int i = 0; i < n; i++){
        curr_strength &= A[i];
        if(curr_strength == 0){
            ans++;
            curr_strength = INT_MAX;
        }
    }

    cout << max(ans, 1) << endl;
}

