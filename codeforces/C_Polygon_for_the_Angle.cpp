void solve(){
    int ang; cin >> ang;
    int g = gcd(180, ang);
    int n = 180 / g;
    int k = ang / g;
    if(k == n - 1){
        n *= 2;
    }

    cout << n << endl;
}

int main(){
    ios::sync_with_stdio(false); cin.tie(nullptr);

    int t = 1;
    cin >> t;
    for(int i = 0; i<t; i++){
        solve();
    }
}